      function showCategory(category) { // 切换选项卡

      	const grid = document.getElementById('ipa-grid');
      	grid.innerHTML = '';

      	ipaCategories[category].forEach(symbolObj => {
      		const itemDiv = document.createElement('div');
      		itemDiv.classList.add('item');
      		itemDiv.textContent = symbolObj.item;
      		itemDiv.onclick = () => playAudio(symbolObj.mp_item);
      		grid.appendChild(itemDiv);
      	});

      	document.querySelectorAll('.tab').forEach(tab => tab.classList.remove('active'));
      	document.getElementById('tab-' + category).classList.add('active');
      }

      function playAudio(mp_item) { //  播放音频
      	const audio = new Audio(mp_item);
      	audio.play();
      }

